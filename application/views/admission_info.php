                    <p>
                        Please be informed that the procedure to take admission of new students will be as follows :
                        <br><br>

                        1. ADMISSION FORM COLLECTION: Parents and applicant should come to school to collect the admission form. The Admission Officer should provide basic information about the school's academic year, facilities, results, ECA programmes, affiliation with Cambridge, etc to the parents
                        <br><br>
                        2. ADMISSION FORM SUBMISSION :<br>
                        a. Applicant should fill in the admission form completely, leaving no field blank in the form. <br>
                        b. With the completed application form, applicant should provide the following to the Admission Officer, Ms Marina Rahman: <br>
                        i. 2 passport-size photographs of applicant and 1 passport size photograph of mother and father each <br>
                        ii. Photocopy of previous school's report card or the transcrips of the full session and the recommendation letter/testimonial/transfer letter/letter from Principal <br>
                        iii. Photocopy of birth certificate of applicant <br>
                        iv. Photocopy of passport of applicant <br>
                        <br><br>
                        <strong>3. ADMISSION FORM ANALYSIS : </strong>Application form will go to the Admission Committee for scrutiny. The Admission Committee will check all information provided in the application form. If the Admission Committee finds that the application form is properly filled in and the applicant has enough credit to get admitted into OIS, the Admission Committee will check the availability of the seat for the desired class. <br>
                        <br>
                        <strong>4. WRITTEN TEST :</strong> If the seat is available, the applicant will be called to sit for a written test in English and Mathematics. <br>
                        <br>
                        <strong>5. INTERVIEW :</strong> If the applicant scores satisfactory marks in the written test, the parents will be called for an interview with the Admission Counselor. The parents should bring the following documents to the interview board: <br>
                        a. Original copy of school transcript of previous class for the whole academic session <br>
                        b. Original copy of testimonial/school leaving certificate/recommendation letter from teacher of previous school <br>
                        c. Original copy of birth certificate of applicant <br>
                        d. Original copy of passport of applicant <br>
                        e. Original voter ID cards of parents <br>
                        f. Parent's Bank statement for the last 6 months 
                        <br><br>
                        <strong>6. RESULTS :</strong> A decision about the admission of the applicant will be circulated in the school website and notice boards <br>
                        <br>
                        <strong>7. ADMISSION :</strong> If result is positive, applicant should come to take admission within 1 week of results publication. <br>
                        <br>
                        <strong>8. WAITING LIST :</strong> Applicants, who are shown in the waiting list, will be considered according to their waiting serials shown in the notice board and in the website. If any seat is declared vacant by the section because of a student's transfer or migration abroad, waiting applicants will be notified in the website and the notice board. Three applicants from the top will get an invitation for admission against one available seat. Invitation will become invalid for two applicants when one applicant from the list gets admission into OIS. </p>
